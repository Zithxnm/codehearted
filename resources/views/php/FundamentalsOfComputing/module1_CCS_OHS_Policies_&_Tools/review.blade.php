                <h2>ðŸ“˜ Learning Materials & Core Lesson</h2>

                <h3>Computer System Servicing (CSS)</h3>
                <p><strong>Computer System Servicing (CSS)</strong> is the process of <strong>installing, maintaining, troubleshooting, and repairing computer systems and networks</strong>. It involves both hardware (the physical parts of a computer) and software (the programs and operating systems that make it work).</p>

                <p>Importance of Computer System Servicing:</p>
                <ul>
                    <li>Ensures <strong>computers are efficient, safe, and secure.</strong></li>
                    <li>Prevents downtime in schools, businesses, and offices.</li>
                    <li>Extends the <strong>lifespan of computers and networks.</strong></li>
                    <li>Provides a career pathway for IT support technicians, network administrators, and system specialists.</li>
                </ul>

                <h3>1. Safety Rules Definition</h3>
                <p><strong>Safety rules</strong> are guidelines and practices designed to protect people from harm and prevent accidents in classrooms, laboratories, or workplaces.</p>
                <ul>
                    <li><strong>Power Off First:</strong> Always shut down and unplug the computer before opening or servicing.</li>
                    <li><strong>Avoid Static Electricity:</strong> Wear an anti-static wrist strap when handling internal components.</li>
                    <li><strong>Use the Right Tool:</strong> Never force a component; use appropriate ICT tools.</li>
                    <li><strong>Organize Work Area:</strong> Keep screws and parts in containers to avoid loss or damage.</li>
                    <li><strong>Handle Components Properly:</strong> Hold circuit boards by the edges, avoid touching chips and connectors.</li>
                    <li><strong>Check for Proper Ventilation:</strong> Prevent overheating during repair or testing.</li>
                    <li><strong>Report Hazards:</strong> Report faulty equipment, exposed wires, or unsafe conditions immediately.</li>
                </ul>

                <h3>2. Personal Protective Equipment (PPE) in CSS</h3>
                <ul>
                    <li><strong>Anti-static Wrist Strap:</strong> Prevents electrostatic discharge (ESD) that can damage computer parts.</li>
                    <li><strong>Safety Goggles:</strong> Protects eyes from accidental sparks, dust, or debris.</li>
                    <li><strong>Face Mask:</strong> Protects from dust during cleaning and prevents inhaling harmful particles.</li>
                    <li><strong>Gloves (ESD or Latex):</strong> Protects hands and components from oils, dirt, or static.</li>
                    <li><strong>Apron/Lab Coat:</strong> Keeps clothes clean and avoids static buildup.</li>
                </ul>

                <h3>3. Common Tools Used in Computer System Servicing</h3>
                <p><strong>Here are the most commonly used tools:</strong></p>

                <div class="image-center">
                    <img src="../../../../../public/css/modules/compfund/mod1/images/tools.png" alt="Computer servicing tools" class="image-center">
                </div>

                <ul>
                    <li><strong>Anti-static Wrist Strap:</strong> Worn on the wrist and clipped to a grounded surface. Prevents static electricity that can damage motherboards, RAM, and other sensitive parts.</li>
                    <li><strong>Screwdriver Set:</strong> Includes flathead, Phillips, and Torx screwdrivers. Used for opening computer cases, drives, and other hardware. Always use the correct size to avoid stripping screws.</li>
                    <li><strong>Tweezers:</strong> A small gripping tool used for handling jumpers, tiny screws, and connectors inside the computer. Useful for tasks where fingers are too large.</li>
                    <li><strong>IC Extractor:</strong> A special tool for safely removing integrated circuits (chips) from their sockets without bending or breaking the pins.</li>
                    <li><strong>Cable Tester:</strong> A small device that checks if network cables (like LAN cables) are working properly. Helps in troubleshooting connectivity issues.</li>
                    <li><strong>Multimeter:</strong> A digital device with probes used to test voltage, resistance, and continuity. Very useful for diagnosing power supply problems.</li>
                    <li><strong>Flash Drive (Bootable):</strong> A USB drive prepared with an operating system or diagnostic software. It is used to install Windows/Linux or run troubleshooting tools.</li>
                    <li><strong>Cleaning Brush:</strong> A soft, anti-static brush used to remove dust from computer components without scratching them.</li>
                    <li><strong>Compressed Air Canister:</strong> A pressurized spray used to blow out dust from fans, keyboards, and inside cases. Must be used carefully (upright only) to avoid moisture release.</li>
                </ul>
